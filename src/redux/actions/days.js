export const changeDaysMin = (daysMin) => ({
  type: 'CHANGE_DAYS_MIN',
  payload: daysMin,
});

export const changeDaysValue = (daysValue) => ({
  type: 'CHANGE_DAYS_VALUE',
  payload: daysValue,
});

export const changeDaysMax = (daysMax) => ({
  type: 'CHANGE_DAYS_MAX',
  payload: daysMax,
});